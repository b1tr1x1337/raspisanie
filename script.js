function init() {
    const data = SCHEDULE_DATA;
    const select = document.getElementById('weekSelect');
    data.weeks.forEach((w, i) => {
        const opt = document.createElement('option');
        opt.value = i;
        opt.textContent = w.title;
        select.appendChild(opt);
    });
    renderSchedule(0);
    select.addEventListener('change', (e) => renderSchedule(parseInt(e.target.value)));

    const themeToggle = document.getElementById('themeToggle');
    const saved = localStorage.getItem('theme');
    if (saved === 'dark') document.body.classList.add('dark');
    themeToggle.addEventListener('click', () => {
        document.body.classList.toggle('dark');
        localStorage.setItem('theme', document.body.classList.contains('dark') ? 'dark' : 'light');
    });

    const bellToggle = document.getElementById('bellToggle');
    const bellSchedule = document.getElementById('bellSchedule');
    bellToggle.addEventListener('click', () => {
        bellSchedule.classList.toggle('open');
    });
}

function renderSchedule(weekIndex) {
    const data = SCHEDULE_DATA;
    const week = data.weeks[weekIndex];
    const container = document.getElementById('schedule');
    container.innerHTML = week.days.map(day => `
        <div class="day-card">
            <h2>${day.name}<span class="date">${day.date}</span></h2>
            ${day.lessons.map(l => `
                <div class="lesson ${l.pair ? '' : 'daily'}">
                    <div class="time">${l.time}</div>
                    <div class="details">
                        <div class="name">${l.name}</div>
                        <div class="meta">
                            ${l.pair ? `<span class="pair">Пара ${l.pair}</span>` : ''}
                            ${l.room ? `<span class="room">Ауд. ${l.room}</span>` : ''}
                            ${l.teacher ? `<span class="teacher">${l.teacher}</span>` : ''}
                        </div>
                    </div>
                </div>
            `).join('')}
        </div>
    `).join('');
}

init();
